def mijn_functie_2(a,b):#L8hw3 functie die lijst van berekeningen maakt en antwoorden in lijstvorm weergeeft
    reken_lijst = []
    reken_lijst.append(a+b)
    reken_lijst.append(a-b)
    reken_lijst.append(a*b)
    reken_lijst.append(a/b)
    return reken_lijst

print(("waarde opdracht 2 ="),(mijn_functie_2(12,3))) #print test voor gecreerde functie L8hw2


